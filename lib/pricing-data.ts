export const IPHONE_MODELS_GROUP_1 = PRICING_GROUP_1.map((p) => p.model);
export const IPHONE_MODELS_GROUP_2 = PRICING_GROUP_2.map((p) => p.model);
export const ALL_MODELS = [...IPHONE_MODELS_GROUP_1, ...IPHONE_MODELS_GROUP_2];

export function getPricing(
  model: string,
  serviceType: "bypass" | "unblock"
): PricingItem | undefined {
  return ALL_PRICING.find(
    (p) => p.model === model && p.serviceType === serviceType
  );
}
